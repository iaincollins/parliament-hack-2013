<?php
/**
 * Represents an event, such as an upcoming debate on a bill.
 *
 *  @version 1.0
 */
 
class Event {
    
    /**
     * @var string  The name of the event
     */
    public $name;
    
    /**
     * @var string  The date of the event
     */
    public $date;
    
    /**
     * @var string  A URL with more information about the event
     */
    public $url;
    
}